export function getDiagnosticProblems(
  grade: LearnerGrade,
  track?: LearnerTrack,
): Problem[] {
  const effectiveGrade = grade === 'unknown' ? 'g1' : grade;

  return problemData.filter((p) => {
    if (p.grade !== effectiveGrade) return false;
    if (effectiveGrade === 'g3' && p.track !== track) return false;
    return true;
  });
}
